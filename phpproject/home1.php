 “Mamata Banerjee is criticising the Balakot air strike for her political interest.
 She is trying to appease the minority voters. Recently,
 her ally (in the ‘mahagathbandhan’), National Conference leader Omar Abdullah,
 said that Kashmir would have a separate prime minister one day. I would like to ask you,
 do you want a separate prime minister in Kashmir? 

